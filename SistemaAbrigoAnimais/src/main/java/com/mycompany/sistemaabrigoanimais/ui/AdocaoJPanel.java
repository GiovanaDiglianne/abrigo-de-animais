    private void carregarTabela() {
        DefaultTableModel dfm = (DefaultTableModel) tabela.getModel();
        dfm.setRowCount(0);
        String termo = buscarJTextField.getText().toLowerCase();

        for (Adocao ad : adocaoDAO.consultar()) {
            String nomeAni = "Desconhecido";
            for(Animal a : animalDAO.consultar()) {
                if(a.getId() == ad.getAnimal().getId()) { nomeAni = a.getNome(); break; }
            }

            if (termo.isEmpty() || ad.getAdotante().toLowerCase().contains(termo) || nomeAni.toLowerCase().contains(termo)) {
                Object[] linha = { ad.getId(), ad.getDataAdocao(), nomeAni, ad.getAdotante() };
                dfm.addRow(linha);
            }
        }
    }
    
