    private void salvarJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarJButtonActionPerformed
        try {
            Adocao ad = new Adocao();
            ad.setDataAdocao(dataAdocaoJTextField.getText());
            ad.setAdotante(adotanteJTextField.getText());

            String nomeSelecionado = animalJComboBox.getSelectedItem().toString();
            Animal animalEncontrado = null;
            for(Animal a : animalDAO.consultar()){
                if(a.getNome().equals(nomeSelecionado)){
                    animalEncontrado = a;
                    break;
                }
            }
            ad.setAnimal(animalEncontrado);

            if (idSelecionado == -1) {
                adocaoDAO.inserir(ad);

                animalEncontrado.setStatus("Adotado");
                animalDAO.atualizar(animalEncontrado);

                for (Consulta c : consultaDAO.consultar()) {
                    if (c.getAnimal().getId() == animalEncontrado.getId()) {
                        consultaDAO.remover(c);
                    }
                }

                JOptionPane.showMessageDialog(this, "Adoção concluída! Animal e Consultas atualizados.");
            } else {
                ad.setId(idSelecionado);
                adocaoDAO.atualizar(ad);
                JOptionPane.showMessageDialog(this, "Dados da adoção atualizados!");
            }

            carregarTabela();
            carregarSeletores(); 
            limparCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_salvarJButtonActionPerformed

    private void editarJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarJButtonActionPerformed
        int linha = tabela.getSelectedRow();
        if (linha == -1) {
            JOptionPane.showMessageDialog(this, "Selecione uma adoção na tabela!");
            return;
        }

        int id = (int) tabela.getValueAt(linha, 0);
        for (Adocao ad : adocaoDAO.consultar()) {
            if (ad.getId() == id) {
                idSelecionado = ad.getId();
                dataAdocaoJTextField.setText(ad.getDataAdocao());
                adotanteJTextField.setText(ad.getAdotante());
                animalJComboBox.setSelectedItem(ad.getAnimal().getNome());
                break;
            }
        }
    }//GEN-LAST:event_editarJButtonActionPerformed

    private void excluirJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirJButtonActionPerformed
        int linha = tabela.getSelectedRow();
        if (linha == -1) return;

        int confirma = JOptionPane.showConfirmDialog(this, "Deseja excluir este registro de adoção?");
        if (confirma == JOptionPane.YES_OPTION) {
            try {
                int id = (int) tabela.getValueAt(linha, 0);
                Adocao ad = new Adocao();
                ad.setId(id);
                adocaoDAO.remover(ad);

                JOptionPane.showMessageDialog(this, "Registro excluído!");
                carregarTabela();
                carregarSeletores();
                limparCampos();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_excluirJButtonActionPerformed

