    private void carregarSeletores() {
        animalJComboBox.removeAllItems();
        for (Animal a : animalDAO.consultar()) {
            if ("Resgatado".equalsIgnoreCase(a.getStatus())) {
                animalJComboBox.addItem(a.getNome());
            }
        }
    }

