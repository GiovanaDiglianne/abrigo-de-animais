    private void salvarJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarJButtonActionPerformed
        try {
            Secretaria s = new Secretaria();
            s.setNome(nomeJTextField.getText());
            s.setCpf(cpfJTextField.getText());
            s.setTelefone(telefoneJTextField.getText());
            s.setEmail(emailJTextField.getText());
            s.setDataNascimento(dataNascimentoJTextField.getText());

            String salarioTexto = salarioJTextField.getText().replace(",", ".");
            s.setSalario(Float.parseFloat(salarioTexto));

            if (idSelecionado == -1) {
                secDAO.inserir(s);
                JOptionPane.showMessageDialog(this, "Secretária cadastrada!");
            } else {
                s.setId(idSelecionado);
                secDAO.atualizar(s);
                JOptionPane.showMessageDialog(this, "Dados atualizados!");
            }
            carregarTabela();
            limparCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_salvarJButtonActionPerformed

    private void editarJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarJButtonActionPerformed
        int linha = tabela.getSelectedRow();
        if (linha == -1) return;

        List<Secretaria> lista = secDAO.consultar();
        Secretaria s = lista.get(linha);

        idSelecionado = s.getId();
        nomeJTextField.setText(s.getNome());
        cpfJTextField.setText(s.getCpf());
        telefoneJTextField.setText(s.getTelefone());
        emailJTextField.setText(s.getEmail());
        dataNascimentoJTextField.setText(s.getDataNascimento());
        salarioJTextField.setText(String.valueOf(s.getSalario()));
    }//GEN-LAST:event_editarJButtonActionPerformed

    private void excluirJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirJButtonActionPerformed
        int linha = tabela.getSelectedRow();
        if (linha == -1) {
            JOptionPane.showMessageDialog(this, "Selecione uma secretária para excluir.");
            return;
        }

        int confirma = JOptionPane.showConfirmDialog(this, "Confirma a exclusão?");
        if (confirma == JOptionPane.YES_OPTION) {
            try {
                int id = (int) tabela.getValueAt(linha, 0); // Pega o ID da primeira coluna
                Secretaria s = new Secretaria();
                s.setId(id);
                secDAO.remover(s);

                JOptionPane.showMessageDialog(this, "Excluído com sucesso!");
                carregarTabela();
                limparCampos();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Erro ao excluir: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_excluirJButtonActionPerformed

