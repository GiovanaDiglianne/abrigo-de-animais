    private void carregarTabela() {
        DefaultTableModel dfm = (DefaultTableModel) tabela.getModel();
        dfm.setRowCount(0);
        String termo = buscarJTextField.getText().toLowerCase();
        String filtro = filtroJComboBox.getSelectedItem().toString();

        List<Secretaria> lista = secDAO.consultar();
        for (Secretaria s : lista) {
            String valorComparar = "";
            if (filtro.equals("Nome")) valorComparar = s.getNome();
            else if (filtro.equals("CPF")) valorComparar = s.getCpf();
            else if (filtro.equals("E-mail")) valorComparar = s.getEmail();

            if (termo.isEmpty() || valorComparar.toLowerCase().contains(termo)) {
                Object[] linha = { 
                    s.getId(),              
                    s.getNome(),             
                    s.getCpf(),             
                    s.getTelefone(),         
                    s.getDataNascimento(),   
                    s.getSalario(),          
                    s.getEmail()             
                };
                dfm.addRow(linha);
            }
        }
    }
    
