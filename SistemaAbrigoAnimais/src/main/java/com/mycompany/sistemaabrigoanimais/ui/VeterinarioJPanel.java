    private void editarJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarJButtonActionPerformed
        int linhaSelecionada = tabela.getSelectedRow();

        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um veterinário na tabela primeiro!");
            return;
        }

        List<Veterinario> lista = vetDAO.consultar();
        Veterinario v = lista.get(linhaSelecionada);
        idSelecionado = v.getId();

        nomeJTextField.setText(v.getNome());
        cpfJTextField.setText(v.getCpf());
        crmvJTextField.setText(v.getCrmv());
        especialidadeJTextField.setText(v.getEspecialidade());
        salarioJTextField.setText(String.valueOf(v.getSalario()));
    }//GEN-LAST:event_editarJButtonActionPerformed

    private void excluirJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirJButtonActionPerformed
        int linhaSelecionada = tabela.getSelectedRow();

        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecione quem deseja excluir.");
            return;
        }

        int confirma = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir este veterinário?");

        if (confirma == JOptionPane.YES_OPTION) {
            try {
                int id = (int) tabela.getValueAt(linhaSelecionada, 0);

                Veterinario v = new Veterinario();
                v.setId(id);
                vetDAO.remover(v);
                JOptionPane.showMessageDialog(this, "Veterinário excluído com sucesso!");
                carregarTabela();
                limparCampos();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Erro ao excluir: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_excluirJButtonActionPerformed


