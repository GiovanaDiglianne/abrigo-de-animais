    private void carregarTabela() {
        DefaultTableModel dfm = (DefaultTableModel) tabela.getModel();
        dfm.setRowCount(0);

        String termo = buscarJTextField.getText().toLowerCase();
        String filtro = filtroJComboBox.getSelectedItem().toString();

        List<Veterinario> lista = vetDAO.consultar();
        for (Veterinario v : lista) {
            String valorComparar = "";
            if (filtro.equals("Nome")) valorComparar = v.getNome();
            else if (filtro.equals("CPF")) valorComparar = v.getCpf();
            else if (filtro.equals("CRMV")) valorComparar = v.getCrmv();
            else if (filtro.equals("Especialidade")) valorComparar = v.getEspecialidade();

            if (termo.isEmpty() || valorComparar.toLowerCase().contains(termo)) {
                Object[] linha = { v.getId(), v.getNome(), v.getCpf(), v.getCrmv(), v.getEspecialidade() };
                dfm.addRow(linha);
            }
        }
    }
