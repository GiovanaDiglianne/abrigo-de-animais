    private void salvarJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarJButtonActionPerformed
        try {
            Veterinario v = new Veterinario();
            v.setNome(nomeJTextField.getText());
            v.setCpf(cpfJTextField.getText());
            v.setCrmv(crmvJTextField.getText());
            v.setEspecialidade(especialidadeJTextField.getText());

            String salarioTexto = salarioJTextField.getText().replace(",", ".");
            v.setSalario(Float.parseFloat(salarioTexto));

            if (idSelecionado == -1) { 
                vetDAO.inserir(v);
                JOptionPane.showMessageDialog(this, "Veterinário cadastrado!");
            } else {
                v.setId(idSelecionado);
                vetDAO.atualizar(v);
                JOptionPane.showMessageDialog(this, "Dados atualizados!");
            }

            carregarTabela();
            limparCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_salvarJButtonActionPerformed

