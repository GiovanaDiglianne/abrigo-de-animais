    private void excluirJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirJButtonActionPerformed
        int linha = tabela.getSelectedRow();
        if (linha == -1) {
            JOptionPane.showMessageDialog(this, "Selecione uma consulta para excluir!");
            return;
        }

        int confirma = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir este agendamento?");
        if (confirma == JOptionPane.YES_OPTION) {
            try {
                int id = (int) tabela.getValueAt(linha, 0);
                Consulta c = new Consulta();
                c.setId(id);

                consultaDAO.remover(c); //

                JOptionPane.showMessageDialog(this, "Consulta excluída!");
                carregarTabela();
                limparCampos();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Erro ao excluir: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_excluirJButtonActionPerformed
    private String buscarNomeAnimal(int id) {
        for(Animal a : animalDAO.consultar()) if(a.getId() == id) return a.getNome();
        return "";
    }
    private String buscarNomeVet(int id) {
        for(Veterinario v : vetDAO.consultar()) if(v.getId() == id) return v.getNome();
        return "";
    }
    private String buscarNomeSec(int id) {
        for(Secretaria s : secDAO.consultar()) if(s.getId() == id) return s.getNome();
        return "";
    }

