    private void salvarJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarJButtonActionPerformed
        try {
            Consulta c = new Consulta();
            c.setDataConsulta(dataConsultaJTextField.getText());

            // Buscamos os objetos completos pelos índices selecionados
            c.setAnimal(animalDAO.consultar().get(animalJComboBox.getSelectedIndex()));
            c.setVeterinario(vetDAO.consultar().get(veterinarioJComboBox.getSelectedIndex()));
            c.setSecretaria(secDAO.consultar().get(secretariaJComboBox.getSelectedIndex()));

            if (idSelecionado == -1) {
                consultaDAO.inserir(c);
                JOptionPane.showMessageDialog(this, "Consulta agendada!");
            } else {
                c.setId(idSelecionado);
                consultaDAO.atualizar(c);
                JOptionPane.showMessageDialog(this, "Consulta atualizada!");
            }
            carregarTabela();
            limparCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_salvarJButtonActionPerformed

    private void editarJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarJButtonActionPerformed
        int linha = tabela.getSelectedRow();
        if (linha == -1) {
            JOptionPane.showMessageDialog(this, "Selecione uma consulta na tabela!");
            return;
        }

        int id = (int) tabela.getValueAt(linha, 0);

        for (Consulta c : consultaDAO.consultar()) {
            if (c.getId() == id) {
                idSelecionado = c.getId();
                dataConsultaJTextField.setText(c.getDataConsulta());

                animalJComboBox.setSelectedItem(buscarNomeAnimal(c.getAnimal().getId()));
                veterinarioJComboBox.setSelectedItem(buscarNomeVet(c.getVeterinario().getId()));
                secretariaJComboBox.setSelectedItem(buscarNomeSec(c.getSecretaria().getId()));
                break;
            }
        }
    }//GEN-LAST:event_editarJButtonActionPerformed

